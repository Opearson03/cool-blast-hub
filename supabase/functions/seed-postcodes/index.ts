Deno.serve(async (req) => {
  if (req.method !== "POST") {
    return new Response("Method not allowed", { status: 405 });
  }

  const supabase = createClient(SUPABASE_URL, SUPABASE_SERVICE_ROLE_KEY);

  // Check if already seeded
  const { count } = await supabase
    .from("au_postcode_coords")
    .select("*", { count: "exact", head: true });

  if (count && count > 100) {
    return new Response(JSON.stringify({ message: `Already seeded with ${count} postcodes` }), {
      headers: { "Content-Type": "application/json" },
    });
  }

  // Batch insert
  const rows = POSTCODES.map(([postcode, locality, lat, lng]) => ({
    postcode,
    locality,
    lat,
    lng,
  }));

  const batchSize = 500;
  let inserted = 0;

  for (let i = 0; i < rows.length; i += batchSize) {
    const batch = rows.slice(i, i + batchSize);
    const { error } = await supabase.from("au_postcode_coords").upsert(batch, { onConflict: "postcode" });
    if (error) {
      return new Response(JSON.stringify({ error: error.message, inserted }), {
        status: 500,
        headers: { "Content-Type": "application/json" },
      });
    }
    inserted += batch.length;
  }

  return new Response(JSON.stringify({ message: `Seeded ${inserted} postcodes` }), {
    headers: { "Content-Type": "application/json" },
  });
});
