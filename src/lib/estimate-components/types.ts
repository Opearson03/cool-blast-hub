// Core types for the modular estimate calculator system

/**
 * Represents a question asked within a component module
 */
export interface ComponentQuestion {
  id: string;
  type: 'number' | 'select' | 'boolean' | 'text' | 'currency';
  label: string;
  helpText?: string;
  defaultValue?: string | number | boolean;
  options?: Array<{ value: string; label: string; priceKey?: string }>;
  priceListKey?: string;  // Links to price list item for auto-fill
  unit?: string;          // Display unit (e.g., 'hrs', 'm³', '$')
  min?: number;
  max?: number;
  step?: number;
  showIf?: (answers: Record<string, any>) => boolean;  // Conditional display
  required?: boolean;
  placeholder?: string;
  
  /** 
   * Function to derive this field's value from scope data, module answers, and price map
   * Returns undefined if no auto-fill should occur
   */
  deriveFrom?: (
    scopeData: Record<string, any>,
    moduleAnswers: Record<string, any>,
    priceMap: PriceMap
  ) => any | undefined;
  
  /**
   * If true, user cannot edit the derived value (read-only)
   * If false (default), user can override the derived value
   */
  derivedReadOnly?: boolean;
}

/**
 * A single line item in the cost breakdown
 */
export interface CostLineItem {
  id: string;
  description: string;
  quantity: number;
  unit: string;
  unitPrice: number;
  total: number;
  category: 'labour' | 'materials' | 'plant' | 'subcontractor' | 'other';
}

/**
 * Result of calculating a component's costs
 */
export interface ComponentCost {
  moduleId: string;
  moduleName: string;
  lineItems: CostLineItem[];
  subtotal: number;
  exclusions: ExclusionItem[];
}

/**
 * An exclusion clause generated by a module
 */
export interface ExclusionItem {
  id: string;
  text: string;
  moduleId: string;
}

/**
 * Price map type for looking up prices by category and item code
 */
export type PriceMap = Record<string, Record<string, number>>;

/**
 * A reusable component module that can be shared across scopes
 */
export interface EstimateModule {
  id: string;
  name: string;
  description?: string;
  icon?: string;
  questions: ComponentQuestion[];
  
  /**
   * Calculate costs based on answers and price list
   * @param answers - User's answers to this module's questions
   * @param priceMap - Price lookup map from business price list
   * @param scopeData - Additional data from scope-level questions (e.g., volume, area)
   */
  calculate: (
    answers: Record<string, any>,
    priceMap: PriceMap,
    scopeData: Record<string, any>
  ) => ComponentCost;
  
  /**
   * Get list of exclusions based on disabled features
   */
  getExclusions: (answers: Record<string, any>) => ExclusionItem[];
  
  /**
   * Validate that all required questions are answered
   */
  validate: (answers: Record<string, any>) => { valid: boolean; errors: string[] };
}

/**
 * Scope-level question (e.g., number of piers, slab dimensions)
 */
export interface ScopeQuestion extends ComponentQuestion {
  affectsModules?: string[];  // Which modules use this value
  calculatesVolume?: boolean; // Does this contribute to concrete volume
}

/**
 * Definition of a scope (e.g., Piers, Slab, Driveway)
 */
export interface ScopeDefinition {
  id: string;
  name: string;
  description?: string;
  icon?: string;
  
  /** Scope-level questions (dimensions, quantities, etc.) */
  questions: ScopeQuestion[];
  
  /** Module IDs that this scope uses */
  moduleIds: string[];
  
  /** Calculate total concrete volume from scope answers */
  calculateVolume?: (answers: Record<string, any>) => number;
  
  /** Default exclusions that always apply to this scope */
  defaultExclusions?: ExclusionItem[];
}

/**
 * Complete estimate state stored in scope_data
 */
export interface EstimateState {
  scopeId: string;
  scopeAnswers: Record<string, any>;
  moduleAnswers: Record<string, Record<string, any>>;
  calculatedCosts: ComponentCost[];
  exclusions: ExclusionItem[];
  customExclusions: ExclusionItem[];
  marginPercent: number;
  subtotal: number;
  marginAmount: number;
  gst: number;
  total: number;
}

/**
 * Helper to create a price map from price list items
 */
export function createPriceMap(
  items: Array<{ category: string; item_code: string; default_price: number; custom_price?: number | null }>
): PriceMap {
  const map: PriceMap = {};
  
  for (const item of items) {
    if (!map[item.category]) {
      map[item.category] = {};
    }
    // Use custom price if set, otherwise default
    map[item.category][item.item_code] = item.custom_price ?? item.default_price;
  }
  
  return map;
}

/**
 * Get a price from the price map with fallback
 */
export function getPrice(priceMap: PriceMap, category: string, itemCode: string, fallback = 0): number {
  return priceMap[category]?.[itemCode] ?? fallback;
}

